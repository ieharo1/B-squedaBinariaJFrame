    public int ordenamiento_y_busqueda_binaria(ArrayList<String> listabb, int b) {

        int[] array = new int[listabb.size()];
        for (int i = 0; i < listabb.size(); i++) {
            array[i] = Integer.parseInt(listabb.get(i));
        }
        Arrays.sort(array);
        //Busqueda Binaria
        ArchivoTextoJFrame archiv = new ArchivoTextoJFrame();
        int centro, primero = 0, ultimo = array.length - 1, valorcentro, cont = 0;
        //Bucle While Busqueda Binaria
        while (primero <= ultimo) {
            centro = (primero + ultimo) / 2;
            valorcentro = array[centro];
            cont++;
            if (b == valorcentro) {
                try {
                    Connection conexion;
                    Statement prep;
                    String respuesta = "insert into interacciones values('"+cont+"','El digito se encontro')";
                    DriverManager.registerDriver(new org.apache.derby.jdbc.ClientDriver());
                    conexion = DriverManager.getConnection("jdbc:derby://localhost:1527/BusquedaBinaria");
                    prep = conexion.createStatement();
                    prep.executeUpdate(respuesta);
                } catch (SQLException ex) {
                    Logger.getLogger(NewClass.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(null, "El digito " + b + " se encontro en " + cont + " pasos");
                return cont;
            } else if (b < valorcentro) {
                ultimo = centro - 1;
            } else {
                primero = centro + 1;
            }
        }
        JOptionPane.showMessageDialog(null, "El elemento no se encontro");
        //Conexion a la BASE DE DATOS

        return -1;
    }

    public int lectura_archivo(File f, int busq) {
        FileReader archivo;
        BufferedReader filtrado;
        try {
            archivo = new FileReader(f);
            filtrado = new BufferedReader(archivo);
            String linea = "";
            ArrayList<String> listabb = new ArrayList<>();
            for (int i = 0; (linea = filtrado.readLine()) != null; i++) {
                listabb.add(linea);
            }
            archivo.close();
            filtrado.close();
            return ordenamiento_y_busqueda_binaria(listabb, busq);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en la lectura del archivo" + e);

        }
        return 0;
    }
