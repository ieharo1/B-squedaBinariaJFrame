    public void guardardatosl1(File f) {
        FileWriter archivo;
        BufferedWriter filtrado;
        String contenido = "";
        try {
            archivo = new FileWriter(f);
            filtrado = new BufferedWriter(archivo);
            for (int i = 0; i < 300; i++) {
                contenido = jList1.getModel().getElementAt(i).toString();
                filtrado.write(contenido, 0, contenido.length());
            }
            filtrado.flush();
            filtrado.close();
            archivo.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en escritura de archivo" + e);
        }
    }

